    private void MostrarDatos() {
        jtxtNombre.setText(personalLog.getNomPerso());
        jtxtApellidos.setText(personalLog.getApePerso());
        jtxtDNI.setText(personalLog.getDniPerso());
        jtxtDireccion.setText(personalLog.getDirecPerso());
        jtxtEmail.setText(personalLog.getEmailPerso());
        jtxtTelefono.setText(personalLog.getFonoPerso());
        jtxtUsuario.setText(personalLog.getUsuPerso());
        jtxtPassword.setText(personalLog.getPassPerso());
    }

    private Personal RecogerDatos() {

        Personal personal;
        int idPerso = personalLog.getIdPerso();
        String nombre = jtxtNombre.getText();
        String apellido = jtxtApellidos.getText();
        String dni = jtxtDNI.getText();
        String direccion = jtxtDireccion.getText();
        String email = jtxtEmail.getText();
        String telefono = jtxtTelefono.getText();
        String usuario = jtxtUsuario.getText();
        String password = jtxtPassword.getText();

        personal = new Personal(idPerso, nombre, apellido, dni, direccion, email, telefono, usuario, password);

        return personal;
    }

