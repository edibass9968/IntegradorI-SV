    private void jtxtDNIKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtDNIKeyPressed
        if (evt.getKeyCode() == evt.VK_ENTER) {
            int key = evt.getKeyChar();
            boolean numeros = key >= 48 && key <= 57;
            if (!numeros) {
                evt.consume();
            }
//EL 8 ES LA CANTIDAD MAX. DE CAR√ÅCTERES PERMITIDOS
            if (jtxtDNI.getText().trim().length() == 8) {
                evt.consume();
            }
            jtxtEmail.requestFocus();
        }
    }//GEN-LAST:event_jtxtDNIKeyPressed

