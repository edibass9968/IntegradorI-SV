    private void Login() {
        String user = jtxtUsuario.getText();
        String pass = String.valueOf(jtxtContra.getPassword());

        String url = "select USU_PERSO, PASS_PERSO, ID_TIPOPER "
                + "from personal where USU_PERSO='" + user + "'";
        try {
            Connection con = conexion.conectar();
            PreparedStatement ps = con.prepareStatement(url);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                //SI EXISTE EL USUARIO
                String u = rs.getString("USU_PERSO");
                String p = rs.getString("PASS_PERSO");
                String i = rs.getString("ID_TIPOPER");
                if (pass.equals(p)) {
                    //VAMOS A EMPLEADO O ADMIN
                    if (i.equals("1")) {
                        PersonalDAO personaldao = new PersonalDAO();
                        List<Personal> personales = personaldao.seleccionar();
                        boolean bandera = false;
                        for (Personal personalLogin : personales) {
                            if (u.equalsIgnoreCase(personalLogin.getUsuPerso()) && p.equalsIgnoreCase(personalLogin.getPassPerso())) {
                                tipoPersonal = personalLogin;
                                personalLogeado = LogDatosPers(personalLogin);
                                Principal principal = new Principal();
                                principal.setVisible(true);
                                bandera = true;
                                this.dispose();
                            }
                        }
                    } else if (i.equals("2")) {
                        PersonalDAO personaldao = new PersonalDAO();
                        List<Personal> personales = personaldao.seleccionar();
                        boolean bandera = false;
                        for (Personal personalLogin : personales) {
                            if (u.equalsIgnoreCase(personalLogin.getUsuPerso()) && p.equalsIgnoreCase(personalLogin.getPassPerso())) {
                                tipoPersonal = personalLogin;
                                personalLogeado = LogDatosPers(personalLogin);
                                Principal_Empleado principal2 = new Principal_Empleado();
                                principal2.setVisible(true);
                                bandera = true;
                                this.dispose();
                            }
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "LA CONTRASEÃ‘A NO ES CORRECTA");
                }
            } else {
                //EL USUARIO NO EXISTE
                JOptionPane.showMessageDialog(null, "EL USUARIO NO EXISTE");
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        }
    }
