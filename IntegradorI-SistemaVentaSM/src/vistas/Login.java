    private Personal LogDatosPers(Personal personal) {
        Personal personalLog;
        int idPerso = personal.getIdPerso();
        String nombre = personal.getNomPerso();
        String apellido = personal.getApePerso();
        String dni = personal.getDniPerso();
        String direccion = personal.getDirecPerso();
        String email = personal.getEmailPerso();
        String telefono = personal.getFonoPerso();
        String user = personal.getUsuPerso();
        String pass = personal.getPassPerso();

        personalLog = new Personal(idPerso, nombre, apellido, dni, direccion, email, telefono, user, pass);
        return personalLog;
    }


